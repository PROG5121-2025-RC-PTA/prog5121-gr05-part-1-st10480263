    public static boolean checkUserName(String fUsername){

        Boolean bFlag = false;
       
       if (fUsername.length() <= 5){
           
           for (int i = 0; 1 > fUsername.length(); i++){
               
               if (fUsername.contains("_")){
                   
                   bFlag = true;
                   break;
               }
            
               else{
                   bFlag = false;
               }
           }     
       }       
       
      else{
           bFlag = false;}
       
      return bFlag;
    }
    
    public static boolean checkPasswordComplexity(String fPassword){

       char CheckCharacter;
        Boolean bFlagUpper = false; 
        Boolean bFlagNum = false;
        Boolean bFlagUnderscore = false;
        Boolean bflag = false;
       
       if (fPassword.length() <= 8){
           
           for (int i = 0; i < fPassword.length(); i++){
               
               CheckCharacter = fPassword.charAt(i);
               
               if (Character.isUpperCase(CheckCharacter)){
                   
                   bFlagUpper = true;
           
               }
               
              else if (Character.isDigit(CheckCharacter)){
                   
                   bFlagNum = true;
                  
               }
               
               else if (Character.isJavaIdentifierPart(CheckCharacter)){
                   
                   bFlagUnderscore = true;
                  
               }
           }     
       }    
       
       return bFlagUpper && bFlagNum && bFlagUnderscore;
    }
    
  
