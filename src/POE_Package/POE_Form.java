    public static Boolean LoginUser(String fUserName, String fPassWord, String fPhoneNumber ){
        
        String UserName = fUserName;
        String PassWord = fPassWord;
        String Phonenumber = fPhoneNumber;
        Boolean bFlagUserName = false;
        Boolean bFlagPassword = false;
        Boolean bFlagPhoneNumber = false;
        Boolean bFlag = false;
        
        for (String aUsername : Username){
            
           if (aUsername.equals(UserName)){
               
               bFlagUserName = true;
               break;
            }    
           
           else{
               bFlagUserName = false;
           }
       }
        
      for (String aPassword : Password){
                 
          if (aPassword.equals(PassWord)){
           
              bFlagPassword = true;
               break;
            }         
          
          else{
              bFlagUserName = false; 
          }
       }   
      
      for (String aPhoneNumber : PhoneNumber){
          
          if(aPhoneNumber.equals(Phonenumber)){
              
              bFlagPhoneNumber = true;
              break;
           }
          
          else{
              bFlagPhoneNumber = false;
          }
        }
      
      if((bFlagPassword == true) && (bFlagPhoneNumber == true) && (bFlagUserName == true)){
          
          bFlag = true;
      }
      
      else{
          bFlag = false;
      }
      
      return bFlag;
    }
    
    public static String ReturnLoginStatus(String fUserName, String fPassWord, String fPhoneNumber){
        
        Boolean LoginStatus = LoginUser(fUserName, fPassWord, fPhoneNumber);
        
        if (LoginStatus = true){
            
            return "User Logged in";
            
        }
        
        else{
            return "User not logged in";
        }     
    }
    
